package com.example.rafael_cruz.prototipo.model;

public class Usuario {
    private String nome,sobreNome, telefone, email,senha,id,linkImgAccount;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getSobreNome() {
        return sobreNome;
    }

    public void setSobreNome(String sobreNome) {
        this.sobreNome = sobreNome;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public String getLinkImgAccount() {
        return linkImgAccount;
    }

    public void setLinkImgAccount(String linkImgAccount) {
        this.linkImgAccount = linkImgAccount;
    }
}
